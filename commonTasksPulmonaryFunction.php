<p>The tasks listed here are common to most of the activities described in
this section.  The amount of time required to perform these tasks is to
included in the time standard for all applicable activities.  Although
the practitioner is referred to this list of tasks as the first step of
most of the activities, the tasks are actually performed at various
stages of the activity as described below:</p>

<p><b>A.  Tasks performed before beginning the activity:</b><br />
Calibrate equipment.<br />
Prepare equipment and supplies.<br />
Verify physician's order for correctness and completeness.<br />
Follow relevant infection control procedures.<br />
Confirm patient identification per institutional policy.<br />
Obtain/review patient history.<br />
Verify clinical indications for the ordered procedure.<br />
Obtain appropriate demographic and physiological data (e.g. birth date,
height and weight).<br />
Gather necessary information via patient interview/questionnaire.<br />
Obtain informed consent if required.<br />
Instruct patient in proper procedure technique.<br />
Attach physiologic monitors as appropriate for the tests ordered.</p>

<p><b>B.  Perform Activity, Monitoring and Coaching the Patient as
Required </b></p>

<p><b>C.  Review data gathered during procedure:</b><br />
Select data for report.<br />
Perform necessary calculations.<br />
Prepare preliminary report.
</p>

<p><b>D.  Post-procedure activities:</b><br />
Monitor patient as required.<br />
Generate final report.<br />
Record observations and assessments in patient's record.<br />
Conduct required communications with health care providers.<br />
Clean equipment and work site.</p>
