        <div class="content"    >
            <p>The tasks listed here are common to most of the activities described in
                this section.  The amount of time required to perform these tasks has
                been included in the time standard for all applicable activities.
                Although the practitioner is referred to this list of tasks as the first
                step of most of the activities, the tasks are actually performed at
                various stages of the activity as described below:</p>

            <p><b>A.  Tasks performed before beginning the activity:</b><br />
                Prepare equipment and supplies.<br />
                Verify physician's order for correctness and completeness.<br />
                Follow relevant infection control procedures.<br />
                Confirm patient identification.<br />
                Obtain/review patient history.<br />
                Verify clinical indications for the ordered procedure.<br />
                Obtain appropriate physiological data (e.g. height and weight).<br />
                Gather necessary information via patient interview/questionnaire.<br />
                Obtain informed consent as required for specific procedures if not
                completed ( stress echo, TEE echo, and contrast echo)Explain procedure
                to patient.<br />
                Attach physiologic monitors.</p>

            <p><b>B.  Perform procedure:</b><br />
                Enter patient data.<br />
                Coach patient to achieve maximal effort if appropriate (e.g. exercise
                testing).<br />
                Make pertinent observations and measurements.</p>

            <p><b>C.  Review data gathered during procedure:</b><br />
                Select data for report.<br />
                Perform necessary calculations.<br />
                Prepare preliminary report.</p>

            <p><b>D.  Post-procedure activities:</b><br />
                Monitor patient as required.<br />
                Generate final report.<br />
                Record observations and assessments in patient's record.<br />
                Conduct required communications with health care providers.<br />
                Clean equipment and work site.</p>
