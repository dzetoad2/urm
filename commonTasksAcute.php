<p>Most of the clinical procedures with time standards identified in this
Manual are accompanied by a list of components of the activity. This
list is to clarify the scope of the activity covered by a specific time
standard. Most of these activities begin by referring to the activities
listed below. The amount of time required to perform these tasks has
been included in the time standards for all applicable activities. This
is a list of steps performed prior to, during, and following most
clinical activities. Although this list of tasks is listed as the first
step in most of the activities, the tasks are actually performed at
various stages of the activity. For easy reference the time standards
assigned to many of the clinical activities in the Manual include the
time necessary to perform this list of common tasks when they are listed
as part of the activities.</p>

<p><b>Tasks Performed Before Seeing the Patient</b><br />
Verify physician's order for correctness and completeness.<br />
Ensure presence and continued relevance of current care plan.<br />
Follow relevant infection control activities (hand washing, glove, gown,
mask, etc.).<br />
Prepare relevant equipment and supplies.<br />
Confirm patient identification per institutional policy.</p>

<p><b>Tasks Completed Prior to Beginning Activity</b><br />
Explain purpose and goals of intended activities to patient. <br />
Provide patient/family education as required. </p>

<p><b>Perform Activity, Monitoring and Coaching the Patient as Required</b></p>

<p><b>Post-treatment Tasks</b><br />
Make pertinent observations and measurements.<br />
Assess impact of care/treatment in achieving desired outcomes.<br />
Record observations and assessments as required and appropriate.<br />
Ensure appropriate communication with other health care workers.<br />
Clean, store, and discard equipment, as appropriate. 
</p>
