<p>The tasks listed here are common to most of the procedures described in
this section.  The amount of time required to perform these tasks has
been included in the time standard for all applicable procedures.
Although the practitioner is referred to this list of tasks as the first
step of most of the procedures, the tasks are actually performed at
various stages of the procedure as described below:</p>

<p><b>A.  Tasks performed before beginning the procedure:</b><br />
Calibrate equipment.<br />
Prepare equipment and supplies.<br />
Verify physician's order for correctness and completeness.<br />
Follow relevant infection control procedures.<br />
Confirm subject identification.<br />
Obtain/review subject history.<br />
Verify clinical indications for the ordered procedure.<br />
Obtain appropriate physiological data (e.g., height and weight).<br />
Gather necessary information via subject interview/questionnaire.<br />
Obtain informed consent as required for specific procedures (e.g.,
attending pulmonary rehab program)<br />
Explain procedure to subject as well as educate family and support
people.<br />
Attach physiologic monitors.</p>

<p><b>B.  Perform procedure:</b><br />
Enter subject data.<br />
Coach subject to achieve maximal effort if appropriate (eg, exercise).<br />
Make pertinent observations and measurements.
</p>

<p><b>C.  Review data gathered during procedure:</b><br />
Select data for report.<br />
Perform necessary calculations.<br />
Prepare technical report.
</p>

<p><b>D.  Post-procedures:</b><br />
Monitor subject as required.<br />
Document technical findings.<br />
Record observations and assessments in subject's record.<br />
Conduct required communications with health care providers.<br />
Clean equipment and work site.<br />
Perform associated billing procedure.</p>
