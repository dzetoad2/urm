<p>The tasks listed here are common to most of the activities described in
this section.  The amount of time required to perform these tasks has
been included in the time standard for all applicable activities.
Although the practitioner is referred to this list of tasks as the first
step of most of the activities, the tasks are actually performed at
various stages of the activity as described below:</p>

<p><b>Tasks Performed Before Seeing the Patient</b><br />
Prepare relevant equipment and supplies.<br />
Calibrate equipment if necessary.<br />
Confirm patient identification per institutional policy.<br />
Obtain/review relevant patient history and gather necessary information
via patient interview/questionnaire.<br />
Verify physician's order for correctness and completeness. <br />
Verify clinical indications are appropriate for the ordered procedure.<br />
Follow relevant infection control activities (hand washing, glove, gown,
mask, etc.).<br />
Attach physiologic monitors as appropriate.<br />
Explain purpose and goals of intended activities to patient. 
</p>

<p><b>Perform procedure:</b><br />
Encourage and coach patient to achieve desired results.<br />
Make pertinent observations and measurements.<br />
Observe safety precautions<br />
Gather data as required</p>

<p><b>Review data gathered during procedure:</b><br />
Select data for report.<br />
Perform necessary calculations.<br />
Prepare preliminary report.</p>

<p><b>Post-procedure activities:</b><br />
Monitor patient as required.<br />
Generate final report.<br />
Record observations and assessments in patient's record.<br />
Conduct required communications with health care providers.<br />
Clean equipment and work site.</p>
